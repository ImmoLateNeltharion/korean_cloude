function makeDictionary(): string[] {
  const unique = new Set<string>();

  for (const term of BASE_TERMS) unique.add(term);

  for (const line of PRODUCT_LINES) {
    unique.add(`vatech ${line}`);
    unique.add(`${line} система`);
    unique.add(`${line} диагностика`);
  }

  for (const line of PRODUCT_LINES) {
    for (const ctx of CONTEXTS) {
      unique.add(`vatech ${line} ${ctx}`);
      if (unique.size >= 300) return Array.from(unique).slice(0, 300);
    }
  }

  for (const term of BASE_TERMS) {
    for (const ctx of CONTEXTS) {
      unique.add(`${term} ${ctx}`);
      if (unique.size >= 300) return Array.from(unique).slice(0, 300);
    }
  }

  return Array.from(unique).slice(0, 300);
}

export const VATECH_DICTIONARY = makeDictionary();
